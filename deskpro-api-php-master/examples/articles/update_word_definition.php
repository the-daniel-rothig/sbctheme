<?php

/**
 * This example updates a glossary word definition.
 */

//-----------------------------------------------------
// DESKPRO API SETTINGS
//-----------------------------------------------------

require __DIR__.'/../../deskpro-api.php';

$deskpro_url   = 'http://example.com/deskpro';   // The URL to your helpdesk
$api_key       = '123:XYZ';                      // Your API key (Admin > Apps > API Keys)

// First, create the API object
$api = new \DeskPRO\Api($deskpro_url, $api_key);

//-----------------------------------------------------
// EXAMPLE VARIABLES
//-----------------------------------------------------

// ID of the definition to update
$definition_id 	= '1';

// New definition
$definition 	= 'Here goes the updated definition';

//-----------------------------------------------------
// EXAMPLE CODE
//-----------------------------------------------------

// Update word definition
$result = $api->articles->updateWordDefinition($definition_id, $definition);

if (!$result->isError()) {
	// Request completed successfully
	$data = $result->getData();
	print_r($data);
} else {
	// Something is wrong . . .  Put on your DEBUG shoes
	echo $result->getErrorMessage();
}
